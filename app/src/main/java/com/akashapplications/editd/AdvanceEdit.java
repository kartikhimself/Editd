    @Override
    public void onClick(View view) {
        switch (view.getId()) {

            case R.id.imgUndo:
                mPhotoEditor.undo();
                break;

            case R.id.imgRedo:
                mPhotoEditor.redo();
                break;

            case R.id.imgSave:
                saveImage(false);
                break;

            case R.id.imgClose:
                onBackPressed();
                break;

            case R.id.imgCamera:
                Intent cameraIntent = new Intent(android.provider.MediaStore.ACTION_IMAGE_CAPTURE);
                startActivityForResult(cameraIntent, CAMERA_REQUEST);
                break;

            case R.id.imgGallery:
                Intent intent = new Intent();
                intent.setType("image/*");
                intent.setAction(Intent.ACTION_GET_CONTENT);
                startActivityForResult(Intent.createChooser(intent, "Select Picture"), PICK_REQUEST);
                break;
        }
    }

    @SuppressLint("MissingPermission")
    private void saveImage(boolean isSharingReqired) {

        File pictureFolder = Environment.getExternalStoragePublicDirectory(
                Environment.DIRECTORY_PICTURES
        );
        File imagesFolder = new File(pictureFolder, "Editd");
        if (!imagesFolder.exists()) {
            imagesFolder.mkdirs();
        }

        File imagesSubFolder = new File(imagesFolder, "AdvanceEditd");
        if (!imagesSubFolder.exists()) {
            imagesSubFolder.mkdirs();
        }

        String destinationFilename = imagesSubFolder.getAbsolutePath() + File.separatorChar+"AdvEditd_"+System.currentTimeMillis()+".png";

        File file = new File(destinationFilename);
        try {

            file.createNewFile();

            SaveSettings saveSettings = new SaveSettings.Builder()
                    .setClearViewsEnabled(true)
                    .setTransparencyEnabled(true)
                    .build();

            mPhotoEditor.saveAsFile(file.getAbsolutePath(), saveSettings, new PhotoEditor.OnSaveListener() {
                @Override
                public void onSuccess(@NonNull String imagePath) {
//                    mPhotoEditorView.getSource().setImageURI(Uri.fromFile(new File(imagePath)));
                    Toast.makeText(getBaseContext(), "Image saved", Toast.LENGTH_SHORT).show();
                    if(isSharingReqired)
                    {
                        final Intent shareIntent = new Intent(Intent.ACTION_SEND);
                        shareIntent.setType("image/*");
                        final File photoFile = new File(imagePath);
                        shareIntent.putExtra(Intent.EXTRA_STREAM, FileProvider.getUriForFile(AdvanceEdit.this, getResources().getString(R.string.file_provider_authorities), photoFile));
                        startActivity(Intent.createChooser(shareIntent, "Share image using"));
                    }
                    else
                    {
                        finish();
                    }
                }

                @Override
                public void onFailure(@NonNull Exception exception) {
                    Toast.makeText(getBaseContext(), "Failed to save image", Toast.LENGTH_SHORT).show();
                    Log.e(Constants.DEBUG_KEY,exception.toString());
                }
            });
        } catch (IOException e) {
            e.printStackTrace();

        }

    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        if (resultCode == RESULT_OK) {
            switch (requestCode) {
                case CAMERA_REQUEST:
                    mPhotoEditor.clearAllViews();
                    Bitmap photo = (Bitmap) data.getExtras().get("data");
                    mPhotoEditorView.getSource().setImageBitmap(photo);
                    break;
                case PICK_REQUEST:
                    try {
                        mPhotoEditor.clearAllViews();
                        Uri uri = data.getData();
                        Bitmap bitmap = MediaStore.Images.Media.getBitmap(getContentResolver(), uri);
                        mPhotoEditorView.getSource().setImageBitmap(bitmap);
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                    break;
            }
        }
    }

